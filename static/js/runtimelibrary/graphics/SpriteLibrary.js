function expandDigits(n, digits){
    let s = "" + n;
    while(s.length < digits) s = "0" + s;
    return s;
}

let spriteLibInit = () => {
    for(let i = 0; i < 56; i++){
        SpriteLibrary.push({
            filename: "explosion-images/explosion" + expandDigits(i, 4) + ".png",
            name: "Explosion_1",
            index: i
        })
    }
    for(let i = 1; i <= 10; i++){
        SpriteLibrary.push({
            filename: "boulders/image_part_" + expandDigits(i, 3) + ".png",
            name: "Boulders",
            index: i - 1
        })
    }
}

